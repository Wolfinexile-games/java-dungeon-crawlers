public class Resistances {

    private int physicalDamResist;
    private int magicDamResist;
    private int blightResist;
    private int fireResist;
    private int bleedResist;
    private int sanityResist;

    public int getBleedResist() {
        return bleedResist;
    }

    public void setBleedResist(int bleedResist) {
        this.bleedResist = bleedResist;
    }

    public int getPhysicalDamResist() {
        return physicalDamResist;
    }

    public void setPhysicalDamResist(int physicalDamResist) {
        this.physicalDamResist = physicalDamResist;
    }

    public int getMagicDamResist() {
        return magicDamResist;
    }

    public void setMagicDamResist(int magicDamResist) {
        this.magicDamResist = magicDamResist;
    }

    public int getBlightResist() {
        return blightResist;
    }

    public void setBlightResist(int blightResist) {
        this.blightResist = blightResist;
    }

    public int getFireResist() {
        return fireResist;
    }

    public void setFireResist(int fireResist) {
        this.fireResist = fireResist;
    }

    public int getSanityResist() {
        return sanityResist;
    }

    public void setSanityResist(int sanityResist) {
        this.sanityResist = sanityResist;
    }
}
